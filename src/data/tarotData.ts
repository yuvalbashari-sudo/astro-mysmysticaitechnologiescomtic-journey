function generateMinorArcana(): TarotCard[] {
  const cards: TarotCard[] = [];
  for (const suit of SUITS) {
    for (let num = 1; num <= 14; num++) {
      const cardName = CARD_NAMES[num];
      const data = MINOR_MEANINGS[suit.name][num];
      cards.push({
        name: `${cardName.en} of ${suit.name}`,
        hebrewName: `${cardName.he} ${suit.hebrew}`,
        number: 22 + (SUITS.indexOf(suit) * 14) + (num - 1),
        symbol: suit.symbol,
        meaning: data.meaning,
        love: data.love,
        career: data.career,
        spiritual: data.spiritual,
        advice: data.advice,
      });
    }
  }
  return cards;
}

const fullDeck: TarotCard[] = [...majorArcana, ...generateMinorArcana()];

export function drawTarotCards(count: number = 3): TarotCard[] {
  const shuffled = [...fullDeck].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(count, fullDeck.length));
}

