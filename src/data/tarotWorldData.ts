export function drawCardsForSpread(spread: SpreadConfig): TarotWorldCard[] {
  // Fisher-Yates shuffle for true randomness
  const shuffled = [...majorArcana];
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }
  return shuffled.slice(0, spread.cardCount);
}

export function getInterpretation(card: TarotWorldCard, spread: SpreadType, position: string): {
  title: string;
  positionLabel: string;
  mainMeaning: string;
  spreadMeaning: string;
  spiritualMessage: string;
  advice: string;
} {
  const m = card.meanings;
  
  let spreadMeaning: string;
  switch (spread) {
    case "daily": spreadMeaning = m.daily; break;
    case "love": spreadMeaning = m.love; break;
    case "career": spreadMeaning = m.career; break;
    case "decision": spreadMeaning = m.decision; break;
    case "universe": spreadMeaning = m.universe; break;
    case "timeline":
      if (position === "העבר") spreadMeaning = m.past;
      else if (position === "ההווה") spreadMeaning = m.present;
      else spreadMeaning = m.future;
      break;
    default: spreadMeaning = m.general;
  }

  return {
    title: card.hebrewName,
    positionLabel: position,
    mainMeaning: m.general,
    spreadMeaning,
    spiritualMessage: m.spiritual,
    advice: m.advice,
  };
}
